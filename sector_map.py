def sector_of(code, sector_map=SECTOR_MAP):
    for sector, codes in sector_map.items():
        if code in codes:
            return sector
    return "其他"


def sector_scores(analyzed, sector_map=SECTOR_MAP):
    """计算各行业得分"""
    from collections import defaultdict
    sector_data = defaultdict(list)
    for a in analyzed:
        s = sector_of(a["code"], sector_map)
        sector_data[s].append(a)
    
    scores = {}
    for sector, items in sector_data.items():
        if not items:
            continue
        avg_score = sum(x["score"] for x in items) / len(items)
        avg_rs = sum(x.get("relative_strength", 0) for x in items) / len(items)
        avg_mom = sum(x.get("momentum_20", 0) for x in items) / len(items)
        # 行业得分：评分 + 相对强度 + 动量
        scores[sector] = {
            "score": round(avg_score + max(0, avg_rs) * 0.5 + max(0, avg_mom) * 0.3, 2),
            "count": len(items),
            "avg_score": round(avg_score, 2),
            "avg_rs": round(avg_rs, 2),
            "avg_mom": round(avg_mom, 2),
        }
    return scores
